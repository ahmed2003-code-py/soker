const عربية_إلى_لاتينية = (s: string) =>
  s.replace(/[٠-٩]/g, (d) => String("٠١٢٣٤٥٦٧٨٩".indexOf(d)));

/** استخراج المبلغ: أكبر رقم منطقي في النص (مع/بدون فواصل) */
export function استخرج_مبلغ(نص: string): { القيمة: string; الثقة: number } | null {
  const ن = عربية_إلى_لاتينية(نص);
  const تطابقات = ن.match(/\d{1,3}(?:[,،]\d{3})+(?:\.\d{1,2})?|\d{3,}(?:\.\d{1,2})?/g);
  if (!تطابقات) return null;
  const قيم = تطابقات
    .map((t) => Number(t.replace(/[,،]/g, "")))
    .filter((n) => Number.isFinite(n) && n >= 100); // الشيكات عادة ≥ 100
  if (!قيم.length) return null;
  const الأكبر = Math.max(...قيم);
  return { القيمة: String(الأكبر), الثقة: 0.55 };
}

/** استخراج تاريخ الاستحقاق ← yyyy-mm-dd */
export function استخرج_تاريخ(نص: string): { القيمة: string; الثقة: number } | null {
  const ن = عربية_إلى_لاتينية(نص);
  let م = ن.match(/(\d{4})[-/](\d{1,2})[-/](\d{1,2})/);
  if (م) {
    const [, y, mo, d] = م;
    return { القيمة: `${y}-${mo.padStart(2, "0")}-${d.padStart(2, "0")}`, الثقة: 0.7 };
  }
  م = ن.match(/(\d{1,2})[-/](\d{1,2})[-/](\d{2,4})/);
  if (م) {
    let [, d, mo, y] = م;
    if (y.length === 2) y = "20" + y;
    return { القيمة: `${y}-${mo.padStart(2, "0")}-${d.padStart(2, "0")}`, الثقة: 0.6 };
  }
  return null;
}

/** استخراج رقم الشيك (سلسلة أرقام 6-14 خانة) */
export function استخرج_رقم_شيك(نص: string): { القيمة: string; الثقة: number } | null {
  const ن = عربية_إلى_لاتينية(نص);
  const تطابقات = ن.match(/\b\d{6,14}\b/g);
  if (!تطابقات) return null;
  // الأطول غالباً رقم الشيك
  const الأطول = تطابقات.sort((a, b) => b.length - a.length)[0];
  return { القيمة: الأطول, الثقة: 0.5 };
}

/** مطابقة اسم البنك من القائمة */
export function طابق_بنك(نص: string): { القيمة: string; الثقة: number } | null {
  for (const ب of البنوك_المصرية) {
    if (نص.includes(ب)) return { القيمة: ب, الثقة: 0.8 };
  }
  return null;
}

export type حقل_مستخرج = { القيمة: string; الثقة: number } | null;
export type حقول_الشيك_المستخرجة = {
  اسم_المدين: حقل_مستخرج;
  المبلغ: حقل_مستخرج;
  المستفيد: حقل_مستخرج;
  محول_من: حقل_مستخرج;
  اسم_البنك: حقل_مستخرج;
  تاريخ_الاستحقاق: حقل_مستخرج;
  رقم_الشيك: حقل_مستخرج;
};

/** بناء الحقول المنظّمة من نص OCR خام */
export function حلّل_نص_الشيك(نص: string): حقول_الشيك_المستخرجة {
  const أسطر = نص
    .split(/\r?\n/)
    .map((s) => s.trim())
    .filter(Boolean);

  // اسم المدين: سطر بعد كلمة مفتاحية، أو أول سطر عربي طويل
  let اسم_المدين: حقل_مستخرج = null;
  let المستفيد: حقل_مستخرج = null;
  for (const س of أسطر) {
    if (/ادفعوا|ادفع|لأمر|إلى|الى/.test(س)) {
      const بعد = س.replace(/.*?(ادفعوا|ادفع|لأمر|إلى|الى)\s*/, "").trim();
      if (بعد.length >= 3 && !المستفيد) المستفيد = { القيمة: بعد, الثقة: 0.45 };
    }
  }
  // أول سطر عربي طويل كاسم مدين محتمل
  const سطر_عربي = أسطر.find((س) => /[؀-ۿ]{3,}/.test(س) && س.length >= 4);
  if (سطر_عربي) اسم_المدين = { القيمة: سطر_عربي, الثقة: 0.4 };

  return {
    اسم_المدين,
    المبلغ: استخرج_مبلغ(نص),
    المستفيد,
    محول_من: null,
    اسم_البنك: طابق_بنك(نص),
    تاريخ_الاستحقاق: استخرج_تاريخ(نص),
    رقم_الشيك: استخرج_رقم_شيك(نص),
  };
}
