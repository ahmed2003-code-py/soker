/**
 * تفقيط: تحويل مبلغ رقمي إلى كلمات عربية (جنيه مصري + قروش).
 * مثال: 185000 → "مئة وخمسة وثمانون ألف جنيه فقط لا غير".
 */

const آحاد = [
  "", "واحد", "اثنان", "ثلاثة", "أربعة", "خمسة", "ستة", "سبعة", "ثمانية", "تسعة",
  "عشرة", "أحد عشر", "اثنا عشر", "ثلاثة عشر", "أربعة عشر", "خمسة عشر",
  "ستة عشر", "سبعة عشر", "ثمانية عشر", "تسعة عشر",
];
const عشرات = ["", "", "عشرون", "ثلاثون", "أربعون", "خمسون", "ستون", "سبعون", "ثمانون", "تسعون"];
const مئات = ["", "مئة", "مئتان", "ثلاثمئة", "أربعمئة", "خمسمئة", "ستمئة", "سبعمئة", "ثمانمئة", "تسعمئة"];

function تحت_الألف(n: number): string {
  const أجزاء: string[] = [];
  const م = Math.floor(n / 100);
  const ب = n % 100;
  if (م) أجزاء.push(مئات[م]);
  if (ب) {
    if (ب < 20) أجزاء.push(آحاد[ب]);
    else {
      const ع = Math.floor(ب / 10);
      const أ = ب % 10;
      if (أ) أجزاء.push(`${آحاد[أ]} و${عشرات[ع]}`);
      else أجزاء.push(عشرات[ع]);
    }
  }
  return أجزاء.join(" و");
}

function مجموعة(n: number, مفرد: string, مثنى: string, جمع: string): string {
  if (n === 0) return "";
  if (n === 1) return مفرد;
  if (n === 2) return مثنى;
  if (n >= 3 && n <= 10) return `${تحت_الألف(n)} ${جمع}`;
  return `${تحت_الألف(n)} ${مفرد}`;
}

function صحيح_إلى_كلمات(n: number): string {
  if (n === 0) return "صفر";
  const أجزاء: string[] = [];
  const مليار = Math.floor(n / 1_000_000_000);
  const مليون = Math.floor((n % 1_000_000_000) / 1_000_000);
  const ألف = Math.floor((n % 1_000_000) / 1000);
  const باقي = n % 1000;

  if (مليار) أجزاء.push(مجموعة(مليار, "مليار", "ملياران", "مليارات"));
  if (مليون) أجزاء.push(مجموعة(مليون, "مليون", "مليونان", "ملايين"));
  if (ألف) أجزاء.push(مجموعة(ألف, "ألف", "ألفان", "آلاف"));
  if (باقي) أجزاء.push(تحت_الألف(باقي));
  return أجزاء.filter(Boolean).join(" و");
}

/** المبلغ بالكلمات بالجنيه المصري */
export function تفقيط(المبلغ: number | string): string {
  const قيمة = Math.abs(Number(المبلغ) || 0);
  const جنيه = Math.floor(قيمة);
  const قروش = Math.round((قيمة - جنيه) * 100);
  const أجزاء: string[] = [];
  if (جنيه > 0) أجزاء.push(`${صحيح_إلى_كلمات(جنيه)} ${جنيه === 1 ? "جنيه" : "جنيهاً"}`);
  if (قروش > 0) أجزاء.push(`${صحيح_إلى_كلمات(قروش)} قرشاً`);
  if (أجزاء.length === 0) return "صفر جنيه فقط لا غير";
  return `${أجزاء.join(" و")} فقط لا غير`;
}
