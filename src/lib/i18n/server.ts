import "server-only";
import { cookies } from "next/headers";
import {
  اسم_كوكي_اللغة,
  اسم_كوكي_المظهر,
  لغة_صالحة,
  مظهر_صالح,
  إنشاء_مترجم,
  اتجاه,
  type لغة,
  type مظهر,
} from "./index";

/** يقرأ اللغة المختارة من الكوكي (افتراضي: عربي). */
export function لغة_الطلب(): لغة {
  return لغة_صالحة(cookies().get(اسم_كوكي_اللغة)?.value);
}

/** يقرأ المظهر المختار من الكوكي (افتراضي: فاتح). */
export function مظهر_الطلب(): مظهر {
  return مظهر_صالح(cookies().get(اسم_كوكي_المظهر)?.value);
}

/** مترجم جاهز للاستخدام في مكوّنات الخادم. */
export function مترجم_الخادم() {
  const ل = لغة_الطلب();
  return { t: إنشاء_مترجم(ل), لغة: ل, اتجاه: اتجاه(ل) };
}
