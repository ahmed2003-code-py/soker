"use client";
import * as React from "react";
import {
  الحوار,
  محتوى_الحوار,
  رأس_الحوار,
  عنوان_الحوار,
  وصف_الحوار,
  تذييل_الحوار,
} from "@/components/ui/dialog";
import { الزر } from "@/components/ui/button";

type الخصائص = {
  مفتوح: boolean;
  عند_التغيير: (مفتوح: boolean) => void;
  العنوان?: string;
  الوصف?: string;
  نص_التأكيد?: string;
  نص_الإلغاء?: string;
  خطر?: boolean;
  عند_التأكيد: () => void | Promise<void>;
};

/** حوار تأكيد للإجراءات الحساسة (حذف، عكس قيد...) */
export function حوار_تأكيد({
  مفتوح,
  عند_التغيير,
  العنوان = "تأكيد العملية",
  الوصف = "هل أنت متأكد؟ لا يمكن التراجع بسهولة عن هذا الإجراء.",
  نص_التأكيد = "تأكيد",
  نص_الإلغاء = "إلغاء",
  خطر = true,
  عند_التأكيد,
}: الخصائص) {
  const [جارٍ, تعيين_جارٍ] = React.useState(false);
  return (
    <الحوار open={مفتوح} onOpenChange={عند_التغيير}>
      <محتوى_الحوار className="max-w-md">
        <رأس_الحوار>
          <عنوان_الحوار>{العنوان}</عنوان_الحوار>
          <وصف_الحوار>{الوصف}</وصف_الحوار>
        </رأس_الحوار>
        <تذييل_الحوار>
          <الزر
            variant={خطر ? "danger" : "default"}
            disabled={جارٍ}
            onClick={async () => {
              try {
                تعيين_جارٍ(true);
                await عند_التأكيد();
                عند_التغيير(false);
              } finally {
                تعيين_جارٍ(false);
              }
            }}
          >
            {جارٍ ? "جارٍ التنفيذ…" : نص_التأكيد}
          </الزر>
          <الزر variant="outline" disabled={جارٍ} onClick={() => عند_التغيير(false)}>
            {نص_الإلغاء}
          </الزر>
        </تذييل_الحوار>
      </محتوى_الحوار>
    </الحوار>
  );
}
