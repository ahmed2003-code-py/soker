/** اختبار ذاتي للمرحلة 8: محلّل OCR للشيكات (مبلغ/تاريخ/بنك/رقم). */
import {
  حلّل_نص_الشيك,
  استخرج_مبلغ,
  استخرج_تاريخ,
  استخرج_رقم_شيك,
  طابق_بنك,
} from "../src/lib/ocr/parse";

function تحقق(ش: boolean, ر: string) {
  if (!ش) throw new Error("فشل: " + ر);
  console.log("✓ " + ر);
}

const نص_شيك = `
البنك الأهلي المصري
فرع المنصورة
ادفعوا لأمر / شركة سكر للتجارة
مبلغ وقدره 185,000.00 جنيه فقط لا غير
التاريخ 15/07/2026
شيك رقم 00123456
`;

function main() {
  // الدوال الفردية
  تحقق(استخرج_مبلغ("القيمة 185,000.00")?.القيمة === "185000", "استخراج المبلغ مع الفواصل");
  تحقق(استخرج_مبلغ("بالأرقام العربية ٥٠٠٠٠")?.القيمة === "50000", "استخراج مبلغ بأرقام عربية");
  تحقق(استخرج_تاريخ("15/07/2026")?.القيمة === "2026-07-15", "تطبيع تاريخ dd/mm/yyyy");
  تحقق(استخرج_تاريخ("2026-7-5")?.القيمة === "2026-07-05", "تطبيع تاريخ yyyy-m-d");
  تحقق(استخرج_رقم_شيك("شيك رقم 00123456")?.القيمة === "00123456", "استخراج رقم الشيك");
  تحقق(طابق_بنك("صادر عن البنك الأهلي المصري")?.القيمة === "البنك الأهلي المصري", "مطابقة البنك");

  // التحليل الكامل
  const ح = حلّل_نص_الشيك(نص_شيك);
  تحقق(ح.المبلغ?.القيمة === "185000", "تحليل كامل: المبلغ 185000");
  تحقق(ح.تاريخ_الاستحقاق?.القيمة === "2026-07-15", "تحليل كامل: التاريخ");
  تحقق(ح.اسم_البنك?.القيمة === "البنك الأهلي المصري", "تحليل كامل: البنك");
  تحقق(ح.رقم_الشيك?.القيمة === "00123456", "تحليل كامل: رقم الشيك");
  تحقق(!!ح.المستفيد?.القيمة && ح.المستفيد.القيمة.includes("سكر"), "تحليل كامل: المستفيد يحوي سكر");

  // التدهور الرشيق: نص فارغ/رديء يعيد قيماً فارغة لا أخطاء
  const فارغ = حلّل_نص_الشيك("xx ?? !!");
  تحقق(فارغ.المبلغ === null && فارغ.تاريخ_الاستحقاق === null, "نص رديء → حقول فارغة بلا أخطاء");
}

try {
  main();
  console.log("\n✅ نجح اختبار المرحلة 8");
  process.exit(0);
} catch (e) {
  console.error("\n❌ فشل اختبار المرحلة 8:", (e as Error).message);
  process.exit(1);
}
